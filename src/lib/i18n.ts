export const translations = { es, en };

const MONTHS_ES = ['ENE','FEB','MAR','ABR','MAY','JUN','JUL','AGO','SEP','OCT','NOV','DIC'];
const MONTHS_EN = ['JAN','FEB','MAR','APR','MAY','JUN','JUL','AUG','SEP','OCT','NOV','DEC'];

/** Format date as MMM/DD/YY (uppercase month, bilingual) */
export function fmtDate(dateStr: string, lang: Lang = 'es'): string {
  const d = new Date(dateStr.length === 10 ? dateStr + 'T00:00:00' : dateStr);
  const months = lang === 'en' ? MONTHS_EN : MONTHS_ES;
  const mmm = months[d.getMonth()];
  const dd = String(d.getDate()).padStart(2, '0');
  const yy = String(d.getFullYear()).slice(-2);
  return `${mmm}/${dd}/${yy}`;
}

/** Format a time as "HH:MM AM/PM" — single source of truth for time
 *  display across the platform. Accepts either a full ISO timestamp or a
 *  bare "HH:MM[:SS]" wall-clock string. The hour is zero-padded and the
 *  meridiem suffix is uppercase (no parentheses), per the platform style
 *  guide. The `lang` arg is kept for API compatibility but the format is
 *  identical in every locale. */
// eslint-disable-next-line @typescript-eslint/no-unused-vars
export function fmtTime(input: string | Date | null | undefined, _lang: Lang = 'es'): string {
  if (!input) return '—';
  let h: number;
  let m: number;
  if (typeof input === 'string') {
    // Bare "HH:MM" or "HH:MM:SS" → no timezone parsing
    if (/^\d{2}:\d{2}(:\d{2})?$/.test(input)) {
      const parts = input.split(':');
      h = parseInt(parts[0], 10);
      m = parseInt(parts[1], 10);
    } else {
      const d = new Date(input);
      if (Number.isNaN(d.getTime())) return '—';
      h = d.getHours();
      m = d.getMinutes();
    }
  } else {
    h = input.getHours();
    m = input.getMinutes();
  }
  const ampm = h >= 12 ? 'PM' : 'AM';
  const h12 = h % 12 || 12;
  const hh = String(h12).padStart(2, '0');
  const mm = String(m).padStart(2, '0');
  return `${hh}:${mm} ${ampm}`;
}

/** Format date + time as "MMM/DD/YY · H:MM (am/pm)". Uses fmtTime for the
 *  time portion so both helpers share a single format. */
export function fmtDateTime(dateStr: string, lang: Lang = 'es'): string {
  const datePart = fmtDate(dateStr, lang);
  const timePart = fmtTime(dateStr, lang);
  return `${datePart} · ${timePart}`;
}

export function getT(lang: Lang) {
  const dict = translations[lang];
  return function t(path: string): string {
    const keys = path.split('.');
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    let val: any = dict;
    for (const k of keys) {
      val = val?.[k];
    }
    if (typeof val === 'string') return val;
    // fallback to ES
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    let fallback: any = translations.es;
    for (const k of keys) {
      fallback = fallback?.[k];
    }
    return typeof fallback === 'string' ? fallback : path;
  };
}
